import pandas as pd
import matplotlib.pyplot as plt
import numpy as np
import os


##########################################################################
# Figure 1
# LE vs GMTP vs Lower Bounds
##########################################################################

def plot_algorithm_comparison(df, capacity, out_dir):

    data = df[df["Drone Capacity"] == capacity]

    targets = data["Targets"].values

    x = np.arange(len(targets))
    width = 0.18

    plt.figure(figsize=(3.4,2.8))

    plt.bar(x-1.5*width,data["LE"],width,label="LE")
    plt.bar(x-0.5*width,data["GMTP"],width,label="GMTP")
    plt.bar(x+0.5*width,data["LB"],width,label="LB")
    plt.bar(x+1.5*width,data["RLB"],width,label="Ref LB")

    plt.xticks(x,targets)

    plt.xlabel("Number of Targets")
    plt.ylabel("TFL (KM)")

    plt.title(f"Capacity = {capacity}")

    plt.grid(axis='y',alpha=0.3)

    plt.legend(fontsize=6)

    plt.tight_layout()

    plt.savefig(os.path.join(out_dir, f"fig1_cap_{capacity}.pdf"), bbox_inches="tight")

    plt.close()


##########################################################################
# Figure 2
# Effect of Capacity
##########################################################################

def plot_capacity_effect(df, targets, out_dir):

    data = df[df["Targets"] == targets]

    cap = data["Drone Capacity"].values

    x = np.arange(len(cap))
    width = 0.18

    plt.figure(figsize=(3.4,2.8))

    plt.bar(x-1.5*width,data["SCP"],width,label="SCD")
    plt.bar(x-0.5*width,data["DCP"],width,label="DCD")
    plt.bar(x+0.5*width,data["LE"],width,label="LE")
    plt.bar(x+1.5*width,data["GMTP"],width,label="GMTP")

    plt.xticks(x,cap)

    plt.xlabel("Drone Capacity")
    plt.ylabel("TFL (KM)")

    plt.title(f"Targets = {targets}")

    plt.grid(axis='y',alpha=0.3)

    plt.legend(fontsize=6)

    plt.tight_layout()

    #plt.savefig(f"fig2_targets_{targets}.pdf",
    #            bbox_inches="tight")
    
    plt.savefig(os.path.join(out_dir, f"fig2_cap_{targets}.pdf"), bbox_inches="tight")


    plt.close()


##########################################################################
# Figure 3
# Approximation Gap
##########################################################################

def plot_gap(df, capacity, out_dir):

    data = df[df["Drone Capacity"] == capacity].copy()

    data["LE Gap"] = \
        (data["LE"]-data["LB"]) \
        /data["LB"]*100

    data["GMTP Gap"] = \
        (data["GMTP"]-data["LB"]) \
        /data["LB"]*100

    targets = data["Targets"].values

    x = np.arange(len(targets))
    width = 0.35

    plt.figure(figsize=(3.4,2.8))

    plt.bar(x-width/2,
            data["LE Gap"],
            width,
            label="LE")

    plt.bar(x+width/2,
            data["GMTP Gap"],
            width,
            label="GMTP")

    plt.xticks(x,targets)

    plt.xlabel("Targets")
    plt.ylabel("Gap (%)")

    plt.title(f"Capacity = {capacity}")

    plt.grid(axis='y',alpha=0.3)

    plt.legend(fontsize=7)

    plt.tight_layout()

    #plt.savefig(f"fig3_gap_cap_{capacity}.pdf",
    #            bbox_inches="tight")
    plt.savefig(os.path.join(out_dir, f"fig3_cap_{capacity}.pdf"), bbox_inches="tight")


    plt.close()


##########################################################################
# Driver Function
##########################################################################

def generate_all_plots(csv_file):

    df = pd.read_csv(csv_file)

    capacities = sorted(df["Drone Capacity"].unique())

    targets = sorted(df["Targets"].unique())

    #########################################################
    # Figure 1
    #########################################################

    for c in capacities:
        plot_algorithm_comparison(df,c)

    #########################################################
    # Figure 2
    #########################################################

    for t in targets:
        plot_capacity_effect(df,t)

    #########################################################
    # Figure 3
    #########################################################

    for c in capacities:
        plot_gap(df,c)

    print("All plots generated successfully.")

def plot_results(excel_file):
    """
    Generate all graphs used in the paper.

    Parameters
    ----------
    excel_file : str
        Excel file generated by the simulation.
    """

    # Read Excel file
    df = pd.read_excel(excel_file)

    out_dir = "Figures"
    os.makedirs(out_dir, exist_ok=True)

    capacities = sorted(df["Drone Capacity"].unique())
    targets = sorted(df["Targets"].unique())

    print("Generating Figure 1 (Algorithm Comparison)...")
    for cap in capacities:
        plot_algorithm_comparison(df, cap, out_dir)

    print("Generating Figure 2 (Capacity Effect)...")
    for tgt in targets:
        plot_capacity_effect(df, tgt, out_dir)

    print("Generating Figure 3 (Approximation Gap)...")
    for cap in capacities:
        plot_gap(df, cap, out_dir)

    print("\nAll figures generated successfully.")

plot_results("Drone_Final_Data.xlsx")